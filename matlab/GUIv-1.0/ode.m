% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
a=str2double(get(handles.edit3,'String'));
b=str2double(get(handles.edit4,'String'));
fa=str2double(get(handles.edit5,'String'));
if isnan(a)||isnan(fa)||isnan(b)
    errordlg('a, b, y(a0) must a value.','Bad Input','modal')
        return
end
try
strf=get(handles.edit1,'String');
f=str2func(['@(x,y)',vectorize([strf,'+0*x'])]);
catch
    errordlg('Unexpected MATLAB expression.','Bad Input','modal')
    return
end
tspan=[a,b];
if a==b
    errordlg('a cannot be equal to b.','Bad Input','modal')
    return;
end
y0=fa;
if handles.way==1
    [x,y]=ode45(f,tspan,y0);
end
if handles.way==2
    [x,y]=ode113(f,tspan,y0);
end
if handles.way==3
    [x,y]=ode23t(f,tspan,y0);
end
if handles.way==4
    [x,y]=ode23s(f,tspan,y0);
end
if handles.way==5
    [x,y]=Improved_Euler(f,tspan,y0);
end
plot(x,y);
grid on;
set(handles.listbox1,'string',num2str([x,y])); 


